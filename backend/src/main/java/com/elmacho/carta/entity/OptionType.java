package com.elmacho.carta.entity;

/**
 * Tipo de opción de producto
 */
public enum OptionType {
    SIZE,           // Tamaño (pequeño, mediano, grande)
    QUANTITY,       // Cantidad (media docena, docena)
    PREPARATION,    // Preparación (a la plancha, frito, al vapor)
    SERVING,        // Porciones/personas (para 1, para 2-3, para compartir)
    COMBO           // Combinación especial
}
